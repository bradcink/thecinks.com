function setMyOptionsAndMap2 (centerLat, centerLng){
  var myOptions2 = {
      zoom: 15,
      center: new google.maps.LatLng(centerLat, centerLng),
      mapTypeId: google.maps.MapTypeId.ROADMAP,
      disableDefaultUI: true
  };
  map2 = new google.maps.Map(document.getElementById("map_canvas2"), myOptions2);
};

function addEventListenerMap2 (newMarkerLat, newMarkerLng) {
  var mapIconLabel = "map-icon-insurance-agency";
  google.maps.event.addListenerOnce(map2, 'idle', function() {
     google.maps.event.trigger(map2, 'resize');
     newMarkerInit(newMarkerLat, newMarkerLng, mapIconLabel);
  });
};

function setPolyOptions (myCoordinates) {
  var polyline = new google.maps.Polyline({
    path: myCoordinates,
    map: map2,
    strokeColor: "#FF0000",
    strokeOpacity: 0.5,
    strokeWeight: 2
  });
  var lengthInMiles = (google.maps.geometry.spherical.computeLength(polyline.getPath()))*(1/1609.344);
  polylineDistances.push(lengthInMiles);
  clearPolylines();
  polylines.push(polyline);
}

// Sets the map on all markers in the array.
function setMapOnAllPolylines(map2) {
  for (var i = 0; i < polylines.length; i++) {
    polylines[i].setMap(map2);
  }
}

// Removes the markers from the map, but keeps them in the array.
function clearPolylines() {
  setMapOnAllPolylines(null);
}

// Shows any markers currently in the array.
function showPolylines() {
  setMapOnAllPolylines(map2);
}

// Deletes all markers in the array by removing references to them.
function deletePolylines() {
  clearPolylines();
  polylines = [];
}

